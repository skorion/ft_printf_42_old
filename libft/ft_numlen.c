#include "includes/libft.h"

size_t	ft_numlen(int n)
{
	size_t	len;

	len = 0;
	if (n < 0)
	{
		len = 1;
		if (n == -2147483648)
			return (11);
		n = -n;
	}
	while (n >= 10 || n <= -10)
	{
		n = n / 10;
		len++;
	}
	return (len + 1);
}
